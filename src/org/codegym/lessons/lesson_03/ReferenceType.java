public class ReferenceType {
    public static void main(String[] args) {
        /**
         * 引用类型
         *
         * 除了基本类型之外，剩下的都是引用类型。
         * 举例，开发中常见的String类型用于存储文本。字符串值需要用双引号引起来。
         * 引用类型的变量类似于C语言的指针，它只保存一个变量/对象的地址（引用），指向某个变量/对象在内存的位置。
         */

        String hello = "Hello_World";
        System.out.println(hello);

    }
}
